<template>
  <button
    :class="[roundKeyClass, getClassForColor]"
    :disabled="disabled"
    @click="$emit('handleKeyClick', keyTitle)"
  >
    {{ keyTitle }}
  </button>
</template>

<script>
export default {
  name: 'RoundKey',
  props: {
    keyTitle: String,
    disabled: Boolean,
  },
  data() {
    return {
      numberColorClass: 'green',
      stringColorClass: 'grey',
      roundKeyClass: 'round-key',
    };
  },
  computed: {
    getClassForColor() {
      if (this.keyTitle === '') {
        return 'blank';
      } else if (parseInt(this.keyTitle) || this.keyTitle === '0') {
        return 'orange';
      }
      return 'grey';
    },
  },
};
</script>

